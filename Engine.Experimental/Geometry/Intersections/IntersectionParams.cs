using System;
using System.Collections.Generic;

namespace Engine
{
    /// <summary>
    /// The intersection params struct.
    /// </summary>
    /// <seealso cref="IEquatable{T}" />
    public struct IntersectionParams : IEquatable<IntersectionParams>
    {
        /// <summary>
        /// Initializes a new instance of the <see cref="IntersectionParams" /> class.
        /// </summary>
        /// <param name="name">The name.</param>
        /// <param name="params">The params.</param>
        public IntersectionParams(string name, string @params)
        {
            this.Name = name;
            this.Params = @params;
        }

        /// <summary>
        /// Gets or sets the name.
        /// </summary>
        /// <value>
        /// The name.
        /// </value>
        public string Name { get; set; }

        /// <summary>
        /// Gets or sets the params.
        /// </summary>
        /// <value>
        /// The parameters.
        /// </value>
        public string Params { get; set; }

        /// <summary>
        /// Implements the operator ==.
        /// </summary>
        /// <param name="left">The left.</param>
        /// <param name="right">The right.</param>
        /// <returns>
        /// The result of the operator.
        /// </returns>
        public static bool operator ==(IntersectionParams left, IntersectionParams right) => left.Equals(right);

        /// <summary>
        /// Implements the operator !=.
        /// </summary>
        /// <param name="left">The left.</param>
        /// <param name="right">The right.</param>
        /// <returns>
        /// The result of the operator.
        /// </returns>
        public static bool operator !=(IntersectionParams left, IntersectionParams right) => !(left == right);

        /// <summary>
        /// Determines whether the specified <see cref="object" />, is equal to this instance.
        /// </summary>
        /// <param name="obj">The <see cref="object" /> to compare with this instance.</param>
        /// <returns>
        ///   <see langword="true"/> if the specified <see cref="object" /> is equal to this instance; otherwise, <see langword="false"/>.
        /// </returns>
        public override bool Equals(object obj) => obj is IntersectionParams @params && Equals(@params);

        /// <summary>
        /// Indicates whether the current object is equal to another object of the same type.
        /// </summary>
        /// <param name="other">An object to compare with this object.</param>
        /// <returns>
        ///   <see langword="true" /> if the current object is equal to the <paramref name="other" /> parameter; otherwise, <see langword="false" />.
        /// </returns>
        public bool Equals(IntersectionParams other) => Name == other.Name && Params == other.Params;

        /// <summary>
        /// Returns a hash code for this instance.
        /// </summary>
        /// <returns>
        /// A hash code for this instance, suitable for use in hashing algorithms and data structures like a hash table. 
        /// </returns>
        public override int GetHashCode()
        {
            var hashCode = 361716329;
            hashCode = hashCode * -1521134295 + EqualityComparer<string>.Default.GetHashCode(Name);
            hashCode = hashCode * -1521134295 + EqualityComparer<string>.Default.GetHashCode(Params);
            return hashCode;
        }
    }
}
